class DefaultListener {
	boolean isReset = true

	/**
	 * Executes before every test case starts.
	 * @param testCaseContext related information of the executed test case.
	 */
	@BeforeTestCase
	def sampleBeforeTestCase(TestCaseContext testCaseContext) {
		println testCaseContext.getTestCaseId()
		println testCaseContext.getTestCaseVariables()

		if (isReset) {
			SetBrowserOption.setDesiredCapability("File Collections/Download" , GlobalVariable.baseUrl)
		}
	}

	/**
	 * Executes after every test case ends.
	 * @param testCaseContext related information of the executed test case.
	 */
	@AfterTestCase
	def sampleAfterTestCase(TestCaseContext testCaseContext, TestSuiteContext testSuiteContext) {
		println testCaseContext.getTestCaseId()
		println testCaseContext.getTestCaseStatus()
		
		if (testCaseContext.getTestCaseStatus() == 'PASSED') {
			isReset = false
		} else {
			isReset = true
			String timestamp = new SimpleDateFormat("yyyyMMdd_HHmmss").format(new Date())
			WebUI.takeScreenshot("${RunConfiguration.getReportFolder()}/Failed Images/${testCaseContext.getTestCaseId()}/${timestamp}.PNG")
			WebUI.closeBrowser()

			try {
				if (DriverFactory.getWebDriver() != null) {
					DriverFactory.closeWebDriver()
					println("WebDriver has been terminated successfully.")
				} else {
					println("No WebDriver instance is running.")
				}
			} catch (Exception e) {
				println("An error occurred while terminating WebDriver: " + e.getMessage())
			}
		}
	}

	/**
	 * Executes before every test suite starts.
	 * @param testSuiteContext: related information of the executed test suite.
	 */
	@BeforeTestSuite
	def sampleBeforeTestSuite(TestSuiteContext testSuiteContext) {
		println testSuiteContext.getTestSuiteId()
	}

	/**
	 * Executes after every test suite ends.
	 * @param testSuiteContext: related information of the executed test suite.
	 */
	@AfterTestSuite
	def sampleAfterTestSuite(TestSuiteContext testSuiteContext) {
		println testSuiteContext.getTestSuiteId()
	}
}